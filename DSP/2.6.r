x1 <- c(0.05,0.81,0.97,0.61,0.34,-0.24)  #まずは加算平均処理
x2 <- c(-0.52,1.28,0.83,0.43,0.16,0.06)
x3 <- c(0.05,0.85,0.89,0.55,0.25,0.13)
x4 <- c(0.08,1.01,0.65,0.56,-0.07,-0.15)
x <- (x1+x2+x3+x4)/4  #加算平均の結果をxとする

y <- c(0,0,0,0) #yを初期化
n <- c(1:4)
for(i in n){
  y[i] = (x[i]+x[i+1]+x[i+2])/3  #MA処理
}

plot(n,y)  #図示
